int	ft_sqrt(int nb)
{
	int n;
	int result;

	n = 0;
	result = 0;
	while (result < nb && nb > 0 && n < 46341)
	{
		result = n * n;
		if (result == nb)
			return (n);
		n++;
	}
	return (0);
}
